public class Personne{
    private String nom;
    private String prenom;
    private int age;

    

    public String getnom(){
        return nom;
    }
    public String getprenom(){
        return prenom;
    }
    public int getage(){
        return age;
    }



    public void setnom(String nom){
        this.nom=nom;
    }
    public void setprenom(String prenom){
        this.prenom=prenom;
    }
    public void setage(int age){
        this.age=age;
    }
}